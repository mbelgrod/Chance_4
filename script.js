document.addEventListener('DOMContentLoaded', () => {
    document.addEventListener('mousemove', placeImage);
});

function placeImage(event) {
    const randomImage = getRandomImage();

    // Create image element
    const imageElement = document.createElement('img');
    imageElement.src = randomImage;

    const scale = .5;

    // Set the scale by adjusting width and height
    imageElement.width = imageElement.width * scale;
    imageElement.height = imageElement.height * scale;

    // Set a random rotation angle between 0 and 360 degrees
    const rotation = Math.random() * 360;

    // Set the position, rotation, and color of the image based on the mouse move event
    imageElement.style.position = 'absolute';
    imageElement.style.left = `${event.clientX - imageElement.width / 2}px`;
    imageElement.style.top = `${event.clientY - imageElement.height / 2}px`;
    imageElement.style.transform = `rotate(${rotation}deg)`;

    // Append the image to the body
    document.body.appendChild(imageElement);
}

function getRandomImage() {
    const randomIndex = Math.floor(Math.random() * imageArray.length);
    return imageArray[randomIndex];
}
